    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String rollno = jTextField1.getText();
        String name = jTextField2.getText();
        String fathername = jTextField3.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "nikul");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
            
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "This Student is already Registered.");
            }
            else{
                if(rollno.equals("") || name.equals("") || fathername.equals("")){
                    JOptionPane.showMessageDialog(null, "All fields are mandatory!!!");
                }
                else{
                    st.executeUpdate("insert into student(rollno, course, branch, name,fathername,gender) values('"+rollno+"','"+course+"','"+branch+"', '"+name+"', '"+fathername+"', '"+gender+"')");
                    JOptionPane.showMessageDialog(null, "Student detail add successfully!");
                    setVisible(false);
                    new adminHome().setVisible(true);
                }
            }
        }
            
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

