    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String dbms = jTextField4.getText();
        String os = jTextField5.getText();
        String dsa = jTextField6.getText();
        
        
        try{
            if(physics.equals("") || maths.equals("") || dbms.equals("") || os.equals("") || dsa.equals("")){
                    JOptionPane.showMessageDialog(null, "All fields are mandatory!!!");
            }
            else{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "nikul");
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
                if(rs.next()){
                     st.executeUpdate("insert into result(rollno, physics, maths, dbms,os,dsa) values('"+rollno+"','"+physics+"','"+maths+"', '"+dbms+"', '"+os+"', '"+dsa+"')");
                    JOptionPane.showMessageDialog(null, "Marks save successfully!");
                    setVisible(false);
                    new addResult().setVisible(true);
                }
            else{
                JOptionPane.showMessageDialog(null, "This Roll No. is not registered. Please register it first.");
            }
            }
        } 
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

